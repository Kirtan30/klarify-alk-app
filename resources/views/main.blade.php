                <h2 class="mt-md-6 mb-md-3">
                    Quelles affections peuvent être traitées par un cabinet médical spécialisé en allergologie?
                </h2>
                <p class="mb-md-4">
                    Les allergies peuvent causer différentes maladies et divers symptômes, qui peuvent parfois être
                    chroniques.
                </p>
                <ul class="list-unstyled mb-md-4">
                    <li class="my-4"><b class="text-primary"><a href="/pages/quest-ce-que-la-rhinite-allergique">Rhinite
                                allergique:</a></b>
                        congestion et/ou écoulement nasal·e et éternuements. La rhinite allergique saisonnière,
                        également connue sous le nom de «&nbsp;rhume des foins&nbsp;», est liée aux allergènes présents
                        en plein air, comme le pollen.
                        Dans le cas d’une rhinite allergique perannuelle, les symptômes peuvent durer toute l’année et
                        être causés par des allergènes présents dans les intérieurs, par&nbsp;exemple, les acariens.
                    </li>
                    <li class="my-4"><b>Conjonctivite allergique (conjonctivite):</b>
                        irritation de la conjonctive ou de la surface de l’œil qui provoque des démangeaisons, une
                        sensation de brûlure, des rougeurs et un larmoiement des yeux.
                    </li>
                    <li class="my-4"><b>Rhinoconjonctivite allergique:</b>
                        éternuements, congestion et écoulement nasal·e ainsi que démangeaisons, sensation de brûlure,
                        rougeurs oculaires et larmoiement.
                    </li>
                    <li class="my-3"><b>Symptômes des voies respiratoires inférieurs:</b>
                        toux, essoufflement, sensation d’oppression dans la poitrine et respiration sifflante.
                    </li>
                    <li class="my-3"><b>Dermatite atopique: </b>
                        maladie chronique ou récidivante (qui revient toujours), également appelée eczéma, caractérisée
                        par des démangeaisons, une sécheresse, des gerçures et des plaies cutanées.
                    </li>
                    <li class="my-3"><b>Réactions cutanées:</b>
                        urticaire, gonflements (angioœdème) ou démangeaisons.
                    </li>
                    <li class="my-3"><b>Réactions gastro-intestinales (allergies alimentaires):</b>
                        nausée, vomissements, ballonnements et diarrhée.
                    </li>
                </ul>
                <p class="mb-4 mb-md-6">
                    Certaines allergies peuvent également provoquer des réactions allergiques potentiellement mortelles.
                    Dans ce cas, ton médecin peut te conseiller de toujours avoir sur toi un kit d’urgence comprenant un
                    auto-injecteur d’adrénaline.
                </p>
                <h2 class="mt-md-6 mb-md-3">Quand dois-je consulter un ou une allergologue?</h2>
                <p class="mb-4 mb-md-6">Il y a deux raisons principales de chercher un cabinet médical spécialisé en
                    allergologie. Les voici:</p>
                <h3 class="mt-md-6 mb-md-3">Diagnostic</h3>
                <p class="mb-md-4">Tu aimerais peut-être savoir quels sont les causes de tes symptômes et faire
                    confirmer tes suspicions d’allergie. Dans ce cas, la première étape serait de faire un test
                    d’allergie.
                    L’allergologue doit tout savoir au sujet de tes symptômes: quand et où ils apparaissent, quelle est
                    leur intensité,
                    combien de temps ils durent et quel pourrait être le facteur déclencheur selon toi. Il serait
                    peut-être judicieux de tenir un journal de bord.
                </p>
                <p class="mb-4 mb-md-6">Tes antécédents médicaux sont aussi importants. Apporte tous les documents
                    importants ainsi qu’une liste de tes médicaments actuels.
                    Ton allergologue te demandera également s’il y a des allergies dans ta famille. Tout cela aide à
                    poser le diagnostic et à définir un plan de traitement.
                </p>
                <h3 class="mt-md-6 mb-md-3">Meilleure gestion des allergies</h3>
                <p class="mb-md-4">Il se peut qu'on t'ait diagnostiqué une allergie, mais qu'elle ne soit pas vraiment
                    maîtrisée. Tu le sauras en faisant notre <a
                        href="https://ch.klarify.me/pages/verificateur-de-symptomes-d-allergie">test d’allergie en
                        ligne</a>.</p>
                <p class="mb-4 mb-md-6">Les allergies peuvent nuire à tes performances au travail ou à l’école. Tu dois
                    peut-être renoncer à certaines activités en plein air, comme le sport.
                    Les symptômes peuvent aussi provoquer de la frustration ou de l’anxiété. Tous ces éléments peuvent
                    indiquer qu’une consultation médicale et un traitement sont nécessaires.
                </p>
                <h2 class="mt-md-6 mb-md-3">Comment mon allergie peut-elle être traitée dans un cabinet médical
                    spécialisé en allergologie?</h2>
                <p class="mb-md-4">
                    Les allergologues sont spécialisé·e·s dans les stratégies permettant aux personnes concernées
                    d’éviter les facteurs déclencheurs et de minimiser les symptômes.
                    Ils ou elles peuvent également prescrire des médicaments plus puissants pour traiter les symptômes,
                    tels que des <a href="https://ch.klarify.me/pages/antihistaminiques">antihistaminiques</a> et des
                    <a href="https://ch.klarify.me/pages/quest-ce-que-la-cortisone">corticostéroïdes</a> disponibles en
                    pharmacie.
                </p>
                <p class="mb-4 mb-md-6">
                    Ou bien on peut te recommander une <a href="https://ch.klarify.me/pages/desensibilisation">immunothérapie
                        allergénique</a>.
                    Ce traitement peut minimiser tes symptômes allergiques à long terme. Le système immunitaire est
                    rééduqué de sorte qu’il ne perçoive plus les facteurs déclencheurs comme une menace.
                </p>
